#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';
import https from 'node:https';

const SOURCE_URL =
    'https://raw.githubusercontent.com/mikey0000/PyMammotion/main/pymammotion/utility/device_type.py';
const OUTPUT_FILE = path.resolve(process.cwd(), 'src/lib/product-keys.ts');

function fetchText(url, redirects = 3) {
    return new Promise((resolve, reject) => {
        https
            .get(url, res => {
                const status = res.statusCode ?? 0;
                const location = res.headers.location;
                if (status >= 300 && status < 400 && location && redirects > 0) {
                    const nextUrl = new URL(location, url).toString();
                    res.resume();
                    resolve(fetchText(nextUrl, redirects - 1));
                    return;
                }
                if (status < 200 || status >= 300) {
                    reject(new Error(`HTTP ${status} while fetching ${url}`));
                    res.resume();
                    return;
                }
                const chunks = [];
                res.on('data', chunk => chunks.push(chunk));
                res.on('end', () => resolve(Buffer.concat(chunks).toString('utf8')));
            })
            .on('error', reject);
    });
}

function parseProductKeyLists(content) {
    const lines = content.split(/\r?\n/);
    const groups = {};
    let activeName = '';
    let activeBuffer = '';
    for (const rawLine of lines) {
        const line = rawLine.trim();
        if (!activeName) {
            const start = line.match(/^([A-Za-z0-9_]+ProductKey)\s*=\s*\[(.*)$/);
            if (!start) {
                continue;
            }
            activeName = start[1];
            activeBuffer = start[2];
        } else {
            activeBuffer += ` ${line}`;
        }

        if (!activeBuffer.includes(']')) {
            continue;
        }

        const keys = [];
        const seen = new Set();
        const re = /["']([^"']+)["']/g;
        let m = re.exec(activeBuffer);
        while (m) {
            const key = m[1];
            if (!seen.has(key)) {
                seen.add(key);
                keys.push(key);
            }
            m = re.exec(activeBuffer);
        }
        groups[activeName] = keys;
        activeName = '';
        activeBuffer = '';
    }
    return groups;
}

function renderTs(groups) {
    const now = new Date().toISOString();
    const sortedEntries = Object.entries(groups).sort(([a], [b]) => a.localeCompare(b));
    const body = sortedEntries
        .map(([name, keys]) => `    ${name}: [${keys.map(k => `'${k}'`).join(', ')}],`)
        .join('\n');

    return `/* eslint-disable @typescript-eslint/naming-convention */
// Auto-generated by scripts/sync-product-keys.mjs
// Source: ${SOURCE_URL}
// Generated at: ${now}

export const PRODUCT_KEY_GROUPS = {
${body}
} as const;

export function resolveProductKeyGroup(productKey: string): string | null {
    if (!productKey) {
        return null;
    }
    for (const [group, keys] of Object.entries(PRODUCT_KEY_GROUPS)) {
        if ((keys as readonly string[]).includes(productKey)) {
            return group;
        }
    }
    return null;
}

export const LUBA_PRO_PRODUCT_KEYS = new Set<string>(PRODUCT_KEY_GROUPS.LubaVProProductKey ?? []);
export const YUKA_MINI_PRODUCT_KEYS = new Set<string>(PRODUCT_KEY_GROUPS.YukaMiniProductKey ?? []);
export const YUKA_ML_PRODUCT_KEYS = new Set<string>(PRODUCT_KEY_GROUPS.YukaMLProductKey ?? []);
export const YUKA_PRODUCT_KEYS = new Set<string>([
    ...(PRODUCT_KEY_GROUPS.YukaProductKey ?? []),
    ...(PRODUCT_KEY_GROUPS.YukaPlusProductKey ?? []),
    ...(PRODUCT_KEY_GROUPS.YukaMVProductKey ?? []),
    ...(PRODUCT_KEY_GROUPS.YukaMN100ProductKey ?? []),
    ...(PRODUCT_KEY_GROUPS.YukaMiniProductKey ?? []),
    ...(PRODUCT_KEY_GROUPS.YukaMLProductKey ?? []),
]);
`;
}

async function main() {
    const content = await fetchText(SOURCE_URL);
    const groups = parseProductKeyLists(content);
    if (!Object.keys(groups).length) {
        throw new Error('No ProductKey groups found in source file');
    }
    const out = renderTs(groups);
    await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
    await fs.writeFile(OUTPUT_FILE, out, 'utf8');
    console.log(`Updated ${path.relative(process.cwd(), OUTPUT_FILE)} with ${Object.keys(groups).length} groups.`);
}

main().catch(err => {
    console.error(err.message || err);
    process.exit(1);
});
